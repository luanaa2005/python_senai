# Crie um algoritmo que solicite ao usuário uma cor (vermelho, verde, azul) e exiba uma mensagem correspondente à cor escolhida

cor = str(input('digite uma cor (vermelho, verde, azul): \n'))

if cor == 'vermelho':
    print('vermelho')
elif cor == 'verde':
    print('verde')
else:
    print('azul')




