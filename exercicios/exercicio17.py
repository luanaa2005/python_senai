# Crie um programa que solicite ao usuário dois números e exiba a soma, subtração, multiplicação e divisão entre eles.

numero1 = float(input('digite o primeiro numero: \n'))
numero2 = float(input('digite o segundo numero: \n'))

print('a soma entre os numeros eh:', numero1 + numero2)
print('a subtracao entre os numeros eh:', numero1 - numero2)
print('a multiplicacao entre os numeros eh:', numero1 * numero2)
print('a divisao entre os numeros eh:', numero1 / numero2)
