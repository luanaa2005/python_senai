# Desenvolva um algoritmo que peça ao usuário para digitar dois números e verifique se a multiplicação deles é igual a 20

numero1 = int(input('Digite um numero: \n'))
numero2 = int(input('Digite outro numero: \n'))


if numero1 * numero2 == 20:
    print('a multiplicacao entre eles eh 20')
else:
    print('a multiplicacao entre eles nao eh 20')
